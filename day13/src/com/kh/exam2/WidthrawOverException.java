package com.kh.exam2;

public class WidthrawOverException extends Throwable {
  public WidthrawOverException() {
    super();
  }

  public WidthrawOverException(String message) {
    super(message);
  }

  public WidthrawOverException(String message, Throwable cause) {
    super(message, cause);
  }

  public WidthrawOverException(Throwable cause) {
    super(cause);
  }

  protected WidthrawOverException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }
}
